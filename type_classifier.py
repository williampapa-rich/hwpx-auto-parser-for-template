def classify_type(q: Question) -> str:
    """Question → 유형 코드. 매칭 실패 시 '미분류' 반환."""
    text = q.question_text or ""
    passage = q.passage_text

    # 1. 번호 기반 우선 매칭 (확실한 시그널)
    num_to_type = {
        # 듣기
        1: "듣기-목적(1)",       2: "듣기-의견(2)",       3: "듣기-요지(3)",
        4: "듣기-그림(4)",       5: "듣기-할일(5)",       6: "듣기-금액(6)",
        7: "듣기-이유(7)",       8: "듣기-언급여부(8)",   9: "듣기-내용일치(9)",
        10: "듣기-도표(10)",
        11: "듣기-짧은응답(11~14)", 12: "듣기-짧은응답(11~14)",
        13: "듣기-짧은응답(11~14)", 14: "듣기-짧은응답(11~14)",
        15: "듣기-할말(15)",     16: "듣기-주제(16)",    17: "듣기-언급된대상(17)",
        # 독해
        18: "목적(18)",          19: "심경(19)",          20: "주장(20)",
        21: "밑줄함의(21)", 22: "요지(22)", 23: "주제(23)", 24: "제목(24)",
        25: "도표(25)", 26: "내용불일치(26)", 27: "안내문(27)",
        28: "어법(28)", 29: "어휘(29)",
        35: "무관문장(35)",
        36: "순서배열(36)", 37: "순서배열(37)",
        38: "문장삽입(38)", 39: "문장삽입(39)",
        40: "요약문(40)",
    }
    if q.number in num_to_type:
        return num_to_type[q.number]
    if q.number is not None and 30 <= q.number <= 34:
        # 30~34는 모두 빈칸. 길이/형태로 세분화
        if "단어" in text or len(passage) < 200:
            return "빈칸-단어(30)"
        return f"빈칸-절({q.number})"
    if q.number == 41 or q.number == 42:
        return "장문어법(41-42)"
    if q.number is not None and 43 <= q.number <= 45:
        return "장문독해(43-45)"

    # 2. 키워드 매칭 (번호 누락 시 fallback)
    for type_code, keywords in TYPE_MAP.items():
        if any(kw in text for kw in keywords):
            return type_code

    # 3. 빈칸 단서
    if q.has_blanks:
        return "빈칸-구(31)"

    # 4. 인라인 마커 (28번류)
    if q.has_inline_markers and "어법" in text:
        return "어법(28)"

    return "미분류"


def annotate_questions(questions: list[Question]) -> list[tuple[Question, str]]:
    """문제 리스트에 유형 코드 부여"""
    return [(q, classify_type(q)) for q in questions]
